export const filtrarProvincias = (idRegion: number): { provincia: string; value: number }[] => {
  return arrProvincias
    .filter((provincia) => provincia.idRegion === idRegion)
    .reduce((acc, curr) => {
      acc.push({ provincia: curr.nombreProvincia, value: curr.idProvincia });

      return acc;
    }, [] as { provincia: string; value: number }[]);
};

